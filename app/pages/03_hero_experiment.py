def compute_log_returns(price_series: list[float]) -> np.ndarray:
    """
    Compute log returns from a price series.

    Args:
        price_series: A list of prices.

    Returns:
        A numpy array of log returns.

    Why this matters:
        Log returns are the standard unit for financial return analysis.
    """
    prices = np.array(price_series, dtype=float)
    prices = prices[prices > 0]
    if len(prices) < 2:
        return np.array([])
    return np.log(prices[1:] / prices[:-1])


def compute_acf_squared(returns: np.ndarray, max_lag: int = 20) -> tuple[list[float], list[int]]:
    """
    Compute the autocorrelation function of squared returns.

    Args:
        returns: A numpy array of log returns.
        max_lag: Maximum lag to compute.

    Returns:
        A (acf_values, lags) tuple.

    Why this matters:
        The ACF of squared returns is the canonical test for volatility clustering.
    """
    squared = returns ** 2
    n = len(squared)
    if n < max_lag + 2:
        return [], []

    mean_sq = np.mean(squared)
    var_sq = np.var(squared)

    if var_sq < 1e-12:
        return [0.0] * max_lag, list(range(1, max_lag + 1))

    acf_values = []
    for lag in range(1, max_lag + 1):
        cov = np.mean((squared[lag:] - mean_sq) * (squared[:-lag] - mean_sq))
        acf_values.append(float(cov / var_sq))

    return acf_values, list(range(1, max_lag + 1))


